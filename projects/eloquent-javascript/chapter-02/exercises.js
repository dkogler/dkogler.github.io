function triangles(num) {
  var out = "";
  for(var i = 0; i < num; i++){
    out += "#";
    console.log(out);
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(start, stop) {
  for (var i = start; i <= stop; i++){
        if (i % 15 === 0){
            console.log("fizzbuzz");
        }
        else if(i % 3 === 0){
            console.log("fizz");
        }
        else if(i % 5 === 0){
            console.log("buzz");
        }
        else {
            console.log(i);
        }
    }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  let board = "";
  let odd = true;
  var next;
  for(var i = 0; i < size; i++){
    next = odd ? false : true;
    for(var j = 0; j < size; j++) {
      board += next ? "#" : " ";
      next = !next;
    }
    odd = !odd;
    board += "\n";
  }
  console.log(board);
}

